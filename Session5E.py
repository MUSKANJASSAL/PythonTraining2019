A = {1, 2, 3, 4}
B = {3, 4, 5, 6}
C = A | B     # Union Operation on Sets
print(C)
D = A & B     # Intersection Operation on Sets
print(D)

X = {1, 2, 3, 4, 5}
Y = {4, 5, 6, 7, 8}
Z = X - Y      # Difference Operations on Sets
print(Z)

Q = {'A', 'B', 'C', 'D', 'E'}
R = {'D', 'E', 'F', 'G', 'H'}
S = Q | R       # Union
print(R)
T = Q & R        # Intersection
print(T)
U = Q - R       # Difference
print(U)